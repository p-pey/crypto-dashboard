const generateSparkline = (trend: 'up' | 'down' | 'neutral' = 'neutral', points: number = 24): number[] => {
  const data: number[] = [];
  let value = 50 + Math.random() * 50;
  
  for (let i = 0; i < points; i++) {
    const trendFactor = trend === 'up' ? 0.02 : trend === 'down' ? -0.02 : 0;
    const change = (Math.random() - 0.5 + trendFactor) * 5;
    value = Math.max(10, Math.min(100, value + change));
    data.push(value);
  }
  
  return data;
};

